class MediaTypeGalleryListSearch(Resource):
    #api-media-type-get-all-gallery
    # @base.access_middleware
    def get(self):
        try:
            #obtenemos todos los tipos de medias
            data = Model()
            data = Model.query.all()
            schema = ModelSchema(exclude=Util.get_default_excludes(), many=True)
            dataMediaType = schema.dump(data)

            dataResponse = []

            for types in dataMediaType:
                aux = {}

                mediaDataCount = db.session.query(func.count(MediaModel.iddef_media_type).label("rows"))\
                .filter(MediaModel.iddef_media_type == types["iddef_media_type"] )

                mediaSchema = GetListMediaSchemaGallery(exclude=Util.get_default_excludes(), many=True)
                mediaData = mediaSchema.dump(mediaDataCount)

                #validamos por el momento que solo salga el nodo con id 1 qu ees el de imagenes0
                if types["iddef_media_type"] == 1 :
                    aux["idasset_type"] = types["iddef_media_type"]
                    aux["tipo"] = types["description"].lower()+"/*"
                    aux["descripcion"] = types["description"]
                    aux["estado"] = types["estado"]
                    aux["rows"] = mediaData[0]["rows"]

                    dataResponse.append(aux)                              
            
            if dataResponse is None:
                response = {
                    "Code": 200,
                    "Msg": "Success",
                    "Error": False,
                    "data": {}
                }
            else:
                response = {
                    "Code": 200,
                    "Msg": "Success",
                    "Error": False,
                    "data": dataResponse
                }
        except Exception as e:
            response = {
                "Code": 500,
                "Msg": str(e),
                "Error": True,
                "data": {}
            }

        return response

